export class Position{
    constructor(x,y,z) {
        this.x = x
        this.y = y
        this.z = z
    }

    setPosition(x,y,z){
        this.x = x
        this.y = y
        this.z = z
    }
    
    setX(x){
        this.x = x
    }

    setY(y){
        this.y = y
    }

    setZ(z){
        this.z = z
    }
}

